// Get saved language from localStorage before initialization
const getInitialLanguage = () => {
  if (typeof window !== 'undefined') {
    const savedLang = localStorage.getItem('i18nextLng')
    if (savedLang && resources[savedLang]) {
      return savedLang
    }
  }
  // Fallback to browser language or English
  return 'en'
}

i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    lng: getInitialLanguage(), // Use saved language or fallback to 'en'
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false,
    },
    detection: {
      // CRITICAL: 'localStorage' must be FIRST
      order: ['localStorage', 'navigator'],
      
      // The key to store the language in
      lookupLocalStorage: 'i18nextLng',
      
      // Cache the user's choice
      caches: ['localStorage'],
    },
  })

export default i18n


